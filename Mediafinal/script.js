let nota1 = parseFloat(prompt("Digite sua primeira nota :"))  // adicionei as 3 variaveis, que são as notas do aluno 
let nota2 = parseFloat(prompt("Digite sua segunda nota :"))
let nota3 = parseFloat(prompt("Digite sua terceira nota :"))
let mediafinal = (nota1*2 + nota2*3 + nota3*5) /10    // aqui fiz uma variavel para armazenar a conta das notas e o resultado
alert ("A sua média final é: "+mediafinal)   // alert para exibir a mensagem/ resultado da média 