let dias = parseInt(prompt("digite o dia da semana: ")) 
if (dias == 1) { //dois iguais, esse é de comparação
    alert("Domingo")
}else
if (dias==2){
    alert("Segunda")
}  
else
if (dias==3){
    alert("Terça")
}  
else
if (dias==4){
    alert("Quarta")
}  
else
if (dias==5){
    alert("Quinta")
}  
else
if (dias==6){
    alert("Sexta")
}  
else
if (dias==7){
    alert("Sabado")
}  
else
 {
    alert("Não há dias disponiveis")
}  
